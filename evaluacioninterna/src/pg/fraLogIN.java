    boolean buscarDatos(String buscar){
        boolean encontrado = false;
        // se pone en un string la secuencia SQL para buscar datos
        String sql = "SELECT * FROM Usuarios WHERE usuario='"+buscar+"'";
        try {
            // se definen instrucciones para la sentencias SQL
            Statement st = conn.createStatement(); // objeto para realizar la conexion
           ResultSet rs = st.executeQuery(sql); // objeto que ejecua el query del SQL
            while (rs.next()) {    //Navega por los registros
                if (txtUsername.getText().equals(rs.getString(1))){ //el get string captura el valor del campo 1 de la tabla
                if(txtPassword.getText().equals(rs.getString(2))){
                    encontrado = true;
                }
            }
        }}catch (Exception e){
            System.out.println(e.getMessage());
        }
        
        return encontrado;
    }
